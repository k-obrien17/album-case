/**
 * Export the owner's top-ranked albums to keithrobrien.com's /collect/albums.
 *
 * Read-only over Turso. Produces a static JSON committed to the kro repo,
 * mirroring media-library/scripts/export-collect-watching.mjs. kro renders
 * it at build time; there is no runtime coupling to this database.
 *
 * Publishes each album's REAL stored `rating` (1-10, 2dp), which is now the
 * primary organizing value of the ranked list. This script used to derive a
 * score from rank position instead -- correct back when ratings didn't exist
 * (see docs/superpowers/specs/2026-07-11-album-score-export-design.md), but
 * wrong now: it invented numbers that drifted from the real ones (e.g. it
 * published 9.98 for an album actually rated 9.99). Publish the data, don't
 * re-derive it.
 *
 * Usage:
 *   node --env-file=web/.env.local web/scripts/export-collect-albums.mjs
 *   TOP_N=20 COLLECT_OUT=/abs/path/albums.json node --env-file=web/.env.local web/scripts/export-collect-albums.mjs
 */
import { createClient } from '@libsql/client';

// Matches web/src/owner.ts's OWNER_ID. Duplicated, not imported: this is a
// plain Node ESM script with no TS loader, so it can't import a .ts file.
const OWNER_ID = 'c0ffee00-0000-4000-8000-000000000001';

const TOP_N = Number(process.env.TOP_N || 10);

function db() {
  const url = process.env.TURSO_DATABASE_URL;
  const authToken = process.env.TURSO_AUTH_TOKEN;
  if (!url || !authToken) {
    console.error('Missing TURSO_DATABASE_URL / TURSO_AUTH_TOKEN. Run `vercel env pull web/.env.local` first.');
    process.exit(1);
  }
  return createClient({ url, authToken });
}

const client = db();
const rows = await client.execute({
  sql: 'SELECT ranking_json FROM ranking_snapshots WHERE session_id = ?',
  args: [OWNER_ID],
});

const row = rows.rows[0];
if (!row) {
  console.error('No ranking snapshot found for the owner session.');
  process.exit(1);
}

import { mkdirSync, writeFileSync, existsSync } from 'node:fs';
import { dirname } from 'node:path';

const ranked = JSON.parse(String(row.ranking_json));

// Hardcoded, like OWNER_ID above: this is a personal, single-machine script.
// Deriving the path from the script's own location (import.meta.url) breaks
// depending on where the repo happens to be checked out -- the main repo vs.
// a temporary git worktree (which nests two levels deeper), producing a
// silently wrong sibling path. A fixed absolute default is more robust, and
// still fully overridable via COLLECT_OUT for any other machine/need.
const OUT =
  process.env.COLLECT_OUT ||
  '/Users/keithobrien/Desktop/Claude/Projects/keithrobrien/content/collect/albums.json';

if (!existsSync(dirname(OUT))) {
  console.error(`Output directory does not exist: ${dirname(OUT)}. Set COLLECT_OUT to a valid path.`);
  process.exit(1);
}

// Fail loudly rather than publish a wrong number: every album in the ranked
// list must carry a real rating (the list is rating-sorted by construction).
const unrated = ranked.slice(0, TOP_N).filter((a) => typeof a.rating !== 'number' || Number.isNaN(a.rating));
if (unrated.length > 0) {
  console.error(`ABORT: ${unrated.length} of the top ${TOP_N} albums have no valid rating:`);
  for (const a of unrated) console.error(`  - ${a.title} (${a.primary_artist_name}) = ${a.rating}`);
  console.error('Nothing was written. Run the rating backfill first.');
  process.exit(1);
}

const top = ranked.slice(0, TOP_N).map((album) => ({
  title: album.title,
  artist: album.primary_artist_name,
  year: album.release_year,
  score: album.rating,
  type: 'album',
  mb_url: `https://musicbrainz.org/release-group/${album.mbid}`,
}));

const payload = {
  generated_at: new Date().toISOString().slice(0, 10),
  source: 'album-case',
  note: 'Generated by album-case/web/scripts/export-collect-albums.mjs. Do not hand-edit.',
  top_albums: top,
};

mkdirSync(dirname(OUT), { recursive: true });
writeFileSync(OUT, JSON.stringify(payload, null, 2) + '\n');
console.log(`Wrote ${top.length} albums to ${OUT}`);

client.close();
